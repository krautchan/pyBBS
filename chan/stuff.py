from termlib import Image, ImgAttr
from termlib.canvas import Canvas
def makeNyanFrames():
    frames = []
    for frame in nyanframes:
        lc = 0
        image = Image()
        for line in frame:
            cc = 0
            for c in line:
                attr = ImgAttr()
                if c == ',': #Blue background
                    attr.background = Canvas.COLOR_BLUE
                    pass
                elif c == '.':#/* White stars */
                    attr.background = Canvas.COLOR_WHITE
                    pass
                elif c == '\'':#/* Black border */
                    attr.background = Canvas.COLOR_BLACK
                    pass
                elif c == '@':#/* Tan poptart */
                    attr.background = Canvas.COLOR_GREY
                    pass
                elif c == '$':#/* Pink poptart */
                    attr.background = Canvas.COLOR_LIGHTRED
                    pass
                elif c == '-':#/* Red poptart */
                    attr.background = Canvas.COLOR_RED
                    pass
                elif c == '>':#/* Red rainbow */
                    attr.background = Canvas.COLOR_RED
                    pass
                elif c == '&':#/* Orange rainbow */
                    attr.background = Canvas.COLOR_LIGHTRED
                    pass
                elif c == '+':#/* Yellow Rainbow */
                    attr.background = Canvas.COLOR_YELLOW
                    pass
                elif c == '#':#/* Green rainbow */
                    attr.background = Canvas.COLOR_GREEN
                    pass
                elif c == '=':#/* Light blue rainbow */
                    attr.background = Canvas.COLOR_LIGHTBLUE
                    pass
                elif c == ';':#/* Dark blue rainbow */
                    attr.background = Canvas.COLOR_BLUE
                    pass
                elif c == '*':#/* Gray cat face */
                    attr.background = Canvas.COLOR_GREY
                    pass
                elif c == '%':#/* Pink cheeks */
                    attr.background = Canvas.COLOR_LIGHTMAGENTA
                    pass
                image.put(attr, (cc*2,lc), ' '*2)
                cc += 1
            lc += 1
        frames.append(image)
    return frames